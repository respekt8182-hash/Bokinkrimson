export const CRIMEA_DISTRICT_BY_SLUG = Object.fromEntries(
  CRIMEA_DISTRICTS.map((d) => [d.slug, d]),
) as Record<string, CrimeaDistrict>;

// Match a district name from Yandex geocoder to our slug.
export function findDistrictSlugByName(name: string): string | null {
  const lower = name.toLowerCase();

  // Prefer urban okrugs when a geocoder string explicitly says "городской ...".
  const urbanBySubstring: Array<[string, string]> = [
    ["сакский город", "saky-urban"],
    ["симферопольский город", "simferopol-urban"],
    ["джанкойский город", "dzhankoy-urban"],
    ["красноперекопский город", "krasnoperekopsk-urban"],
  ];

  for (const [token, slug] of urbanBySubstring) {
    if (lower.includes(token)) {
      return slug;
    }
  }

  // Handle adjectival urban-okrug forms and special Sevastopol district names.
  const aliasBySubstring: Array<[string, string]> = [
    ["алуштинск", "alushta-urban"],
    ["феодосийск", "feodosiya-urban"],
    ["ялтинск", "yalta-urban"],
    ["балаклавск", "balaklavsky-sevastopol"],
    ["гагаринск", "gagarinsky-sevastopol"],
    ["нахимовск", "nakhimovsky-sevastopol"],
  ];

  for (const [token, slug] of aliasBySubstring) {
    if (lower.includes(token)) {
      return slug;
    }
  }

  // In some OSM datasets Gagarinsky district appears as "Західний/Западный".
  if ((lower.includes("західн") || lower.includes("западн")) && lower.includes("севастоп")) {
    return "gagarinsky-sevastopol";
  }

  // Avoid collision with Leninsky district of Crimea.
  if (lower.includes("ленинск") && lower.includes("севастоп")) {
    return "leninsky-sevastopol";
  }

  for (const d of CRIMEA_DISTRICTS) {
    if (lower.includes(d.shortName.toLowerCase())) {
      return d.slug;
    }
  }
  return null;
}
