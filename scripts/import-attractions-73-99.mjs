function assertUniqueSlugs(overrides) {
  const slugToIds = new Map();
  for (const [id, attraction] of Object.entries(overrides)) {
    const ids = slugToIds.get(attraction.slug) ?? [];
    ids.push(id);
    slugToIds.set(attraction.slug, ids);
  }

  const duplicates = [...slugToIds.entries()].filter(([, ids]) => ids.length > 1);
  if (duplicates.length > 0) {
    throw new Error(`Duplicate attraction slugs: ${duplicates.map(([slug, ids]) => `${slug}: ${ids.join(", ")}`).join("; ")}`);
  }
}

async function main() {
  const [detailedMarkdown, overviewMarkdown, overridesRaw] = await Promise.all([
    readFile(detailedSeoPath, "utf8"),
    readFile(overviewSeoPath, "utf8"),
    readFile(overridesPath, "utf8"),
  ]);

  if (!overviewMarkdown.includes("Водопады, мысы и бухты Крыма")) {
    throw new Error("Unexpected overview SEO file content.");
  }

  const pages = new Map(splitDetailedPages(detailedMarkdown).map((page) => [page.title, page]));
  const overrides = JSON.parse(overridesRaw);

  for (const entry of detailedEntries) {
    const page = pages.get(entry.sourceTitle);
    if (!page) {
      throw new Error(`SEO page not found: ${entry.sourceTitle}`);
    }
    overrides[entry.id] = await buildDetailedAttraction(entry, page, overrides[entry.id]);
  }

  for (const entry of manualEntries) {
    overrides[entry.id] = await buildManualAttraction(entry, overrides[entry.id]);
  }

  assertUniqueSlugs(overrides);
  await writeFile(overridesPath, `${JSON.stringify(overrides, null, 2)}\n`, "utf8");
  console.log(`Imported ${detailedEntries.length + manualEntries.length} attractions and image galleries.`);
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
